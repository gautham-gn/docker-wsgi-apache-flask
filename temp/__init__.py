@app.route('/forecast/<date_given>', methods=['GET'])
def getForecastTemp(date_given):
    date_given = str(date_given)
    index_list = []

    for items in tempDB:
        for key,value in items.iteritems():
            for num in ("2013","2014","2015","2016","2017","2018"):
                if key == 'DATE' and value == num + date_given[4:]:
                    temp = []
                    temp = filter(lambda t: t['DATE'] == value, tempDB)
                    ind = tempDB.index(temp[0])
                    index_list.append(ind)
    
    if len(index_list) == 0:
        abort(404)
    else:
        new_data_list = []
    
        for number in range(0, 7):
            tmax = 0
            tmin = 0
            for index in range(0, len(index_list)):
                if number == 0:
                    index_list[index] = index_list[index]
                if number > 0:
                    index_list[index] = index_list[index] + 1
            data_list = []
            for ind in index_list:
                if ind < len(tempDB):
                    data_list.append(tempDB[ind])
            for items in data_list:
                for key, value in items.iteritems():
                    if key == "DATE":
                        pred_temp = {"DATE": date_given[0:4] + value[4:]}
                    if key == 'TMAX':
                        tmax = tmax + value
                    if key == 'TMIN':
                        tmin = tmin + value
            pred_temp["TMAX"] = float("%.2f" % (tmax / len(index_list)))
            pred_temp["TMIN"] = float("%.2f" % (tmin / len(index_list)))
            new_data_list.append(pred_temp)
    return jsonify(new_data_list)
    
